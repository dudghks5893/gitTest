package testEx.solo;

public class Codup {

	public static void main(String[] args) {
        Integer sc = new Integer(75254);        
        String a = sc.toString();
        char [] arr = a.toCharArray();   
        for(int i=0; i<arr.length; i++) {
            System.out.print("[" + arr[i]);
            for(int j=arr.length-1; j>i; j--) { 
                System.out.print("0");          
            }
            System.out.println("]");
        }
	}

}
