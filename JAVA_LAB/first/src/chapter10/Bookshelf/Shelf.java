package chapter10.Bookshelf;

import java.util.ArrayList;

public class Shelf {
	
	protected ArrayList<String> shelf; //리스트 선언 (객체변수)
	
	public Shelf() { //생성자
		shelf = new ArrayList<String>(); //리스트 생성
	}
	public ArrayList<String> getShelf(){
		return shelf; //shelf 어레이리스트를 리턴한다.
	}
	public int getCount() { //리스트 사이즈
		return shelf.size();
	}
}
